			<p>At the age of eleven, he played part of a female in a play, and was soon directing plays, such as the Ramlila in several towns and villages.The film world beckoned and RG went to Bombay to try his luck there but his heart was probably not in it.  According to an interview he gave to The Statesman: ‘Every day on my way to and from the film studio I used to see the signboard of the J.J. School of Art. The word “art” fascinated me greatly. One day I just walked in. I didn’t know what I wanted to be but I didn’t have to make a decision. Admissions were open to the architecture class only. I filled in the form and, after an interview was admitted.’ Theatre then took a backseat as RG did his studies, went back to Lahore and set up an architectural firm with partner U.M. Aptay.</p>
