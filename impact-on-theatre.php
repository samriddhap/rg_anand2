			<p>Anand wrote plays in Hindi or Urdu-Hindi (Hindustani, as it was commonly known at the time, and Farsi) with crisp, lively dialogue and believable characters, directed with flair, getting the best out of his cast (some of his plays involved large casts of as many as thirty performers), innovated with stage design, and made untiring efforts to professionalize theatre, as well as to establish a quality auditorium for the staging of drama. A hallmark of RG as a theatre man was his attention to detail. </p><p>His endeavour was to focus equally on all aspects of drama, from script writing to lyrics and music, from dialogues to diction, from stage design and props to costumes, from acting to dances. This was what lay behind his impulse and ability to attract a range of talented people – lyricists, singers, designers and other artists – as collaborators to the Delhi stage.  </p>
			<p>Comedy was his forte, and while his underlying themes were always serious, imbued with a deep social concern, he had no qualms about incorporating song and dance and other popular elements in a play to increase its appeal.</p><p> RG himself made no bones about the nature of the theatre he produced – it was popular entertainment, and there was no pretence of writing or producing literary or avant-garde drama. His motivation was purely to attract an audience for theatre and to do what he loved. There could have been no commercial incentive since, far from earning any money from his plays, he was putting his own money into them! And perhaps this kind of theatre was exactly what was required during the period. </p>
