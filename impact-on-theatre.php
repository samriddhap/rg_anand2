			<span>Priyamvada Kapur   </span>
			<hr>
			<p>RG’s daughter Priyamvada Kapur reminiscences: ‘At around five, after work, people would start trickling in and rehearsals would begin. It was one big mela every evening! On days before the actual show, rehearsals would go on all day, with lunch being served in the garden. So much hustle and bustle, actors fluffing their lines and everyone bursting out in laughter… It was such fun for us kids to watch! Passersby often stopped to watch as well, and they were served tea and snacks too.’ She recalls her mother Romeshlatta being very supportive of father’s passion, uncomplainingly taking care of things that needed to be done.</p>
			<p> ‘Only occasionally would she mildly voice objections to the money being spent on the endless rounds of tea!’ </p>
            
