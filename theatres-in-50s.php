			<h2>Theatre in the 50s</h2>
			<hr>
            <p>RG’s first play, Vivah Bandhan, was performed on a stage erected in a restaurant, Metro, on Queensway (now Janpath). The lack of venues to stage plays was one of numerous difficulties plaguing theatre of the time. In the 1950s Delhi had only the open-air Wavell Theatre at Janpath (which had to be equipped with folding chairs and covered with a marquee for performances), Sapru House and the Fine Arts Theatre which charged prohibitive rents, and a small 150-seater hall at YMCA. </p>
            <p>Alternatively one had to hire Dining Halls of Metro Restaurant … or Standard Restaurant (then known as Davico) and convert them to make-believe Theatre Halls; but worst of all was the cajoling, blackmailing, pleading and promising one had to do in order to sell a few tickets, if only to cover some of the basic expenses, and gather a sprinkling of an audience for the shows.</p>
