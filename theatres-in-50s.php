        <p>Indraprastha Natya Mandal started with translated works of prominent writers. Gradually, RG began to write Hindi and Punjabi plays in Farsi, a script he was most familiar with. His plays had a local context displaying remarkable insight into the Indian character.</p>
			<p>Theatre required many players, and finding volunteer actors in those days was no easy task, especially for plays requiring a large cast. With no remuneration possible (only lyricists, musicians and some other technical people were paid), actors were in it solely for the love of theatre and acting, making time for it amidst their other commitments. ‘In the middle of rehearsals one actor was likely to disappear for weeks together, another had to attend some wedding, yet another fell sick, and by the time the rehearsals were complete, one of the actors might leave Delhi under his transfer.  And of course there were no training schools for actors. RG patiently groomed many actors. There were always fresh talents to mould for his new plays. Under his tutelage many achieved stardom, including KS Hoon and his daughter Sudha (Chopra) and Rama Johar,  Abhimanyu Sharma, Pamela (Chopra), Ram Avtar, S K Mahan, B.S. Thapa, Madan Bala Sindhu, musician Bhupinder, Damyanti Puri, Nirmal Kanta, Arun Bali, Kulbhushan Kharbanda, Ashok Sarin, Kamleshwar Gill, Mohan Kainth, Pritvanti Dubey, Ashok Dhawan, to name a few. RG was no mean actor himself, and played minor roles in some of his plays, often stealing the show. He was also known for his powerful narration of plays, beautifully bringing each character to life. He was a gifted story teller, often writing stories and articles on social issues which were published, and broadcast on All India Radio. </a></p>
