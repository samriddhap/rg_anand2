				<h2>Pioneer Architect and Dramatist Par Excellence</h2> 
				<hr>  
			</div>  
			<p>R.G. Anand's long and distinguished career in the field of architecture and theatre started in 1939 in Lahore and spanned three decades in Independent India from 1950 to 1982. </p>
			<p>He was the first Punjabi architect to set up practice in the newly formed capital. Fondly known as "R.G." by his many admirers and friends, he worked assiduously on his first love, theatre, and nurtured the theatre movement in Delhi, injecting vitality and spirit into it, with his unbridled enthusiasm and passionate obsession for this form of the performing arts. </p>
			<p>He was one of the foremost organizers of Punjabi and Hindi theatre in the capital, producing over 30 plays, of which 18 were scripted by him. A man of many interests, he also taught at the School of Town Planning and Architecture, and still found enough time to serve several cultural organizations in the capital.He was one of the foremost organizers of Punjabi and Hindi theatre in the capital, producing over 30 plays, of which 18 were scripted by him. A man of many interests, he also taught at the School of Town Planning and Architecture, and still found enough time to serve several cultural organizations in the capital.</p>
            
